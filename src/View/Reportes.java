    private JTextField cuiClienteField;
    private JButton generarDepositosButton;
    private JButton generarRetirosButton;

    public Reportes(ReportesControler reportesController) {
        setTitle("Generar Reportes");
        setSize(400, 150);
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        setLocationRelativeTo(null);

        JPanel panel = new JPanel();
        panel.setLayout(new GridLayout(3, 2));

        panel.add(new JLabel("CUI del Cliente:"));
        cuiClienteField = new JTextField();
        panel.add(cuiClienteField);

        generarDepositosButton = new JButton("Generar Reporte de Dep√≥sitos");
        generarDepositosButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String cuiCliente = cuiClienteField.getText();
                reportesController.generarReporteDepositos(cuiCliente);
            }
        });
        panel.add(generarDepositosButton);

        generarRetirosButton = new JButton("Generar Reporte de Retiros");
        generarRetirosButton.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String cuiCliente = cuiClienteField.getText();
                reportesController.generarReporteRetiros(cuiCliente);
            }
        });
        panel.add(generarRetirosButton);

        add(panel);
    }

